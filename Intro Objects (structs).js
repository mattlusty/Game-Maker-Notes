// - An object/struct is a collection of properties
// - These properties "belong" to the object
// - Each property consists of a key: value pair

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// # Confusing Note! 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////

// - Technically ALL entities in an object/struct are called "properties" (even the methods)...
// - However sometimes they are divided into two categories when being referred to ...

// 1) "Properties": properties which contain a value ... such as a number, string, array or even another object
// 2) "Methods":    properties which contain a function! 
